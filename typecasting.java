//type casting -> assigning a value of one type to a variable of another type
// Two types of type casting -> 1) Widening  2) Narrowing
//Widening -> chote data type se bade data type ki taraf
//Narrowing -> bade data type se chote data type ki taraf
//Order: byte,short,integer,long,float,double
public class typecasting{
    public static void main(String[] args) {
        int a=10;
        double b=10.0;
        int c=(int)b;
        System.out.println(a);
        System.out.println(c);


        
    }
}
